package flow;

public class Result {	
	// 构造函数
    public Result() {
        super();
    }	
    
	String noun;
	String data;
	String category;
	
	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getNoun() {
		return noun;
	}

	public void setNoun(String noun) {
		this.noun = noun;
	}
	

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}


    
}
